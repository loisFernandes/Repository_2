public class Numbers 
{	
		public int GetSum (int a, int b)
		{
			int sum;
			sum = a+b;
			return sum;
		}
		
		
        public int GetRemainder (int c, int d)
		{
			int remainder;
			remainder = c/d;
			return remainder;
		}
        
        
        public int GetProduct (int x, int y, int z)
		{
			int product;
			product = x*y*z;
			return product;
		}
}

